package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		double a = 4.5;
		System.out.println(a);
		
		// se eu criar uma variavel com var o java vai definir o tipo pelo valor inserido
		// porem ele nao permite mudar o tipo da variavel
		var b = 4.5;
		System.out.println(b);
		var texto = "testando";
		System.out.println(texto);
		
	}
}
